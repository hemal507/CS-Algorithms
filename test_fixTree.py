import fixTree

def test_case1() :
	assert fixTree.fixTree(["      *  ",  "    *    ",  "***      ",  "    *****",  "  *******",  "*********",  " ***     "]) == ["    *    ",  "    *    ",  "   ***   ",  "  *****  ",  " ******* ",  "*********",  "   ***   "]

def test_case2() :
        assert fixTree.fixTree(["*",  "*",  "*",  "*"]) == ["*",  "*",  "*",  "*"]

def test_case3() :
        assert fixTree.fixTree(["   *** "]) == ["  ***  "]

def test_case4() :
        assert fixTree.fixTree(["         *   ",  "*            ",  "       ***   ",  "   *****     ",  "  *******    ",  "  *********  ",  "*******      ",  "   ********* ",  "  ***********",  "    *********",  "***********  ",  "*************",  "        ***  ",  "  ***        ",  "       ***   "]) == ["      *      ",  "      *      ",  "     ***     ",  "    *****    ",  "   *******   ",  "  *********  ",  "   *******   ",  "  *********  ",  " *********** ",  "  *********  ",  " *********** ",  "*************",  "     ***     ",  "     ***     ",  "     ***     "]

